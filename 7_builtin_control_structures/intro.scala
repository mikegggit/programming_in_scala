/*
there's only a handful of built-in control structures.

- if
- while
- for
- try
- match
- function calls

scala favors enabling developers to create their own structures and
to store them in libs.

scala built-ins almost always result in a value.

A control structure almost always results in a value.

Having control structures return values simplifies client
code by removing the need to use temporary variables to 
store the result of calculations performed in a control structure.
*/
